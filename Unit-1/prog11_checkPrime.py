#program-11 check number is prime or not

num = int(input("Enter any number:"))
# print((num//2)+1)

if num > 1:

    for i in range(2, (num//2)+1):
        if (num % i) == 0:
            print("*********************************************************************")
            print(num, "is not a prime number")
            print("*********************************************************************")
            break
    else:
        print("*********************************************************************")
        print(num, "is a prime number")
        print("*********************************************************************")
else:
    print("*********************************************************************")
    print(num, "is not a prime number")
    print("*********************************************************************")